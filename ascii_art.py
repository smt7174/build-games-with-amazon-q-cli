"""
時限爆弾ゲームで使用するアスキーアート
"""

def get_bomb_art():
    """爆弾のアスキーアート"""
    return """
      ,--.!,
   __/   -*-
 ,d08b.  '|`
 0088MM
 `9MMP'
    """

def get_explosion_art():
    """爆発のアスキーアート"""
    return """
        ,--.!,
     __/   -*-
   ,d08b.  '|`   BOOM!
   0088MM      /
   `9MMP'     *
      |\\  * 
    * |_\\  *
   *        *
  *    *    *
    *    *
  *    *   *
    """

def get_defused_art():
    """解除成功のアスキーアート"""
    return """
      ,--.!,
   __/   -*-
 ,d08b.  '|`  DEFUSED!
 0088MM     ✓
 `9MMP'
    """

def get_timer_art(seconds):
    """タイマーのアスキーアート"""
    return f"""
  .------------------.
 /  {seconds:02d}:{seconds%60:02d}  \\
/                    \\
|     .---------.    |
|    /           \\   |
|   |             |  |
|    \\           /   |
|     '---------'    |
\\                    /
 \\                  /
  '------------------'
    """

